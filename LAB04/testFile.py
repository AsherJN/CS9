from Stack import Stack
from lab04 import *

def test_solveMaze():
    maze = [
        ['+', '+', '+', '+', 'G', '+'],
        ['+', ' ', '+', ' ', ' ', '+'],
        ['+', ' ', ' ', ' ', '+', '+'],
        ['+', ' ', '+', '+', ' ', '+'],
        ['+', ' ', ' ', ' ', ' ', '+'],
        ['+', '+', '+', '+', '+', '+']]
    assert solveMaze(maze, 4, 4) == True

    maze = [[]]

    assert solveMaze(maze, 4, 4) == False

    maze = [[]]
    assert solveMaze(maze,0,0) == False

    maze = [
        ['+', '+', '+', '+', '+', '+'],
        ['+', ' ', ' ', ' ', ' ', '+'],
        ['+', ' ', ' ', 'G', ' ', '+'],
        ['+', ' ', ' ', ' ', ' ', '+'],
        ['+', ' ', ' ', ' ', ' ', '+'],
        ['+', '+', '+', '+', '+', '+']]
    assert solveMaze(maze, 4, 4) == True

    maze = [
        ['+', '+', '+', '+', '+', '+'],
        ['+', ' ', ' ', ' ', ' ', '+'],
        ['+', ' ', ' ', ' ', ' ', '+'],
        ['+', ' ', ' ', ' ', ' ', '+'],
        ['+', ' ', ' ', ' ', ' ', '+'],
        ['+', '+', '+', '+', '+', '+']]
    assert solveMaze(maze, 4, 4) == False

    maze = [
        ['+', '+', '+', '+', '+', '+', '+'],
        ['+', ' ', ' ', ' ', ' ', 'G', '+'],
        ['+', '+', '+', '+', '+', '+', '+']]

    assert solveMaze(maze, 1, 1) == True

    maze = [
        ['+', '+', '+', '+', '+', '+'],
        ['+', '+', '+', '+', ' ', '+'],
        ['+', '+', '+', '+', ' ', '+'],
        ['+', '+', '+', '+', ' ', '+'],
        ['+', 'G', ' ', ' ', ' ', '+'],
        ['+', '+', '+', '+', '+', '+']]

    assert solveMaze(maze, 4, 4) == True